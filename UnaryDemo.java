package firstprogram;

public class UnaryDemo {

	public static void main(String[] Args) {

		int result = +4;
		System.out.println(result);
		// increase the of result by 1
		result++;
		System.out.println(result);
		// decrease the value of result by 1
		result--;
		System.out.println(result);
		// change result value to negative
		result = -4;
		System.out.println(result);
		// return the value of result to positive
		result = +4;
		System.out.println(result);

		boolean power = true;
		// true
		System.out.println(power);
		// false
		System.out.println(!power);

		boolean success = false;
		System.out.println(!success);

	}

}
